#!/usr/bin/env node
/**
 * Bake the repository's pairings/*.json files into a single TS module
 * re-exporting the typed array. Runs before `tsup` so the bundler sees
 * the current data.
 *
 * Writes:
 *   src/pairings.generated.ts  — typed default export for the bundler
 *   dist/pairings.json         — raw JSON for the published tarball
 */
import fs from 'node:fs';
import path from 'node:path';
import url from 'node:url';

const __dirname = path.dirname(url.fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..', '..', '..');
const PAIRINGS_DIR = path.join(ROOT, 'pairings');
const SRC_DIR = path.join(__dirname, '..', 'src');
const OUT_DIR = path.join(__dirname, '..', 'dist');

fs.mkdirSync(OUT_DIR, { recursive: true });

const files = fs
  .readdirSync(PAIRINGS_DIR)
  .filter((f) => f.endsWith('.json') && !f.startsWith('_'))
  .sort();

const pairings = [];
for (const file of files) {
  const text = fs.readFileSync(path.join(PAIRINGS_DIR, file), 'utf-8');
  pairings.push(JSON.parse(text));
}

const json = JSON.stringify(pairings, null, 2);

const tsModule = `/* eslint-disable */
// Auto-generated by scripts/bake-pairings.mjs. Do not edit by hand.
import type { Pairing } from './types.js';
const data: Pairing[] = ${json};
export default data;
`;

fs.writeFileSync(path.join(SRC_DIR, 'pairings.generated.ts'), tsModule);
fs.writeFileSync(path.join(OUT_DIR, 'pairings.json'), json);

console.log(`baked ${pairings.length} pairing(s) → src/pairings.generated.ts + dist/pairings.json`);
